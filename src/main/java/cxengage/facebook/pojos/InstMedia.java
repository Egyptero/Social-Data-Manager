package cxengage.facebook.pojos;

public class InstMedia {
	private String media_url;
	private String media_type;
	private String permalink;
	private int like_count;
	private InstUser owner;
	private String caption;
	private int comments_count;
	private String id;
	private InstComments comments;
	private String timestamp;
	private boolean updated;
	private boolean route = false;
	public int getComments_count() {
		return comments_count;
	}
	public void setComments_count(int comments_count) {
		this.comments_count = comments_count;
	}

	public String getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getMedia_url() {
		return media_url;
	}
	public void setMedia_url(String media_url) {
		this.media_url = media_url;
	}
	public String getMedia_type() {
		return media_type;
	}
	public void setMedia_type(String media_type) {
		this.media_type = media_type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getLike_count() {
		return like_count;
	}
	public void setLike_count(int like_count) {
		this.like_count = like_count;
	}
	public InstComments getComments() {
		return comments;
	}
	public void setComments(InstComments comments) {
		this.comments = comments;
	}
	public InstUser getOwner() {
		return owner;
	}
	public void setOwner(InstUser owner) {
		this.owner = owner;
	}
	@Override
	public String toString() {
		return "InstMedia\n [media_url=" + media_url + ", media_type=" + media_type + ", like_count=" + like_count
				+ ", owner=" + owner + ", caption=" + caption + ", comments_count=" + comments_count + ", id=" + id
				+ ", comments=" + comments + ", timestamp=" + timestamp + "]";
	}
	public boolean isUpdated() {
		return updated;
	}
	public void setUpdated(boolean updated) {
		this.updated = updated;
	}
	public boolean isRoute() {
		return route;
	}
	public void setRoute(boolean route) {
		this.route = route;
	}
	public String getPermalink() {
		return permalink;
	}
	public void setPermalink(String permalink) {
		this.permalink = permalink;
	}

}

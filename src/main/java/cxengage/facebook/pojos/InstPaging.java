package cxengage.facebook.pojos;

public class InstPaging {
	private InstCursors cursors;
	private String next;
	private String previous;

	public InstCursors getCursors() {
		return cursors;
	}

	public void setCursors(InstCursors cursors) {
		this.cursors = cursors;
	}

	@Override
	public String toString() {
		return "InstPaging\n [cursors=" + cursors + "]";
	}

	public String getNext() {
		return next;
	}

	public void setNext(String next) {
		this.next = next;
	}

	public String getPrevious() {
		return previous;
	}

	public void setPrevious(String previous) {
		this.previous = previous;
	}

}
